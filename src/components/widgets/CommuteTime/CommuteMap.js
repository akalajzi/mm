const DirectionsGoogleMap = withGoogleMap(props => (
  <GoogleMap
    defaultZoom={11}
    defaultCenter={props.center}
    defaultOptions={{
			styles: darkStyle,
			mapTypeControl: false,
			zoomControl: false,
			scaleControl: false,
			streetViewControl: false,
			rotateControl: false,
			fullscreenControl: false,
		}}
  >
    {/* [{props.directions && <DirectionsRenderer directions={props.directions} />}, <TrafficLayer autoUpdate />] */}
    <TrafficLayer autoUpdate />
  </GoogleMap>
));

export default class CommuteMap extends Component {
  constructor(props) {
    super(props)

    this.state = {
      origin: this.props.origin,
      destination: this.props.destination,
      directions: null,
    }
  }

  componentDidMount() {
    const DirectionsService = new google.maps.DirectionsService();

    DirectionsService.route({
      origin: this.state.origin,
      destination: this.state.destination,
      travelMode: google.maps.TravelMode.DRIVING,
    }, (result, status) => {
      if (status === google.maps.DirectionsStatus.OK) {
        this.setState({
          directions: result,
        });
      } else {
        console.error(`error fetching directions ${result}`);
      }
    });
  }

  render() {
    const center = {
      lat: (this.props.origin.lat + this.props.destination.lat) / 2,
      lng: (this.props.origin.lng + this.props.destination.lng) / 2,
    }
    return (
      <DirectionsGoogleMap
        containerElement={
          <div style={{ height: `100%` }} />
        }
        mapElement={
          <div style={{ height: `100%` }} />
        }
        center={center}
        directions={this.state.directions}
      />
    );
  }
}
